// ssd1306.cpp — SSD1306 OLED controller implementation.
//
// On real hardware this drives the SSD1306 over I2C (the MVSR's
// Wire1 bus, GPIO 17/18, address 0x3C — see board_t3s3_mvsr.h).
// On the host build the I2C calls are stubbed and the framebuffer
// is the test fixture.
//
// The embedded 8×8 font is the public-domain font8x8_basic by
// Daniel Hepper (github.com/dhepper/font8x8, CC0 / public domain),
// trimmed to the ASCII printable range 0x20–0x7F. Each glyph is 8
// bytes, one per row, MSB = leftmost pixel.

